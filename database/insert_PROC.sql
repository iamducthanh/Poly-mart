-- Thống kê doanh thu, ngại viết service
IF OBJECT_ID('THONG_KE_DOANH_THU') IS NOT NULL
DROP PROC THONG_KE_DOANH_THU
GO
CREATE PROC THONG_KE_DOANH_THU
	@YEAR INT,
	@MONTH INT
AS
	DECLARE @ID_THEOTHANG TABLE(
		ID INT
	)
	INSERT INTO @ID_THEOTHANG SELECT * FROM HOADONTHANHTOAN WHERE YEAR(NGAYTHANHTOAN) = @YEAR AND MONTH(NGAYTHANHTOAN) = @MONTH

    SELECT SUM((GIABAN - GIAVON) * CHITIETHOADONTHANHTOAN.SOLUONG) AS DOANHTHU, 
    SUM(CHITIETHOADONTHANHTOAN.SOLUONG) AS SOLUONGBAN
    FROM CHITIETHOADONTHANHTOAN JOIN CHITIETSANPHAM 
    ON CHITIETHOADONTHANHTOAN.IDCHITIETSANPHAM = CHITIETSANPHAM.ID 
    WHERE CHITIETHOADONTHANHTOAN.IDHOADONTHANHTOAN IN (SELECT * FROM @ID_THEOTHANG)

