		IDCHITIETSP INT,
		SOLUONGBAN INT
	)

	INSERT INTO @THONGKEDOANHSO 
    SELECT IDCHITIETSANPHAM, SUM(SOLUONG) AS SOLUONGBAN FROM 
	CHITIETHOADONTHANHTOAN JOIN HOADONTHANHTOAN 
	ON CHITIETHOADONTHANHTOAN.IDHOADONTHANHTOAN = HOADONTHANHTOAN.ID
	WHERE YEAR(NGAYTHANHTOAN) = 2021 AND MONTH(NGAYTHANHTOAN) = 4
	GROUP BY IDCHITIETSANPHAM

	SELECT IDSANPHAM, TEN, SIZE, MAUSAC, SOLUONGBAN FROM @THONGKEDOANHSO AS THONGKEDOANHSO JOIN CHITIETSANPHAM 
	ON CHITIETSANPHAM.ID = THONGKEDOANHSO.IDCHITIETSP JOIN SANPHAM
	ON SANPHAM.ID = CHITIETSANPHAM.IDSANPHAM

